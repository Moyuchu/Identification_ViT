class h_sigmoid(nn.Module):
    def __init__(self, inplace=True):
        super(h_sigmoid, self).__init__()
        self.relu = nn.ReLU6(inplace=inplace)

    def forward(self, x):
        return self.relu(x + 3) / 6


class h_swish(nn.Module):
    def __init__(self, inplace=True):
        super(h_swish, self).__init__()
        self.sigmoid = h_sigmoid(inplace=inplace)

    def forward(self, x):
        return x * self.sigmoid(x)

class swish(nn.Module):
    def __init__(self):
        super(swish, self).__init__()

    def forward(self, x):
        return x * F.sigmoid(x)

class Swish(nn.Module):
    def __init(self, inplace=True):
        super(Swish, self).__init__()
        self.inplace = inplace

    def forward(self, x):
        if self.inplace:
            x.mul_(torch.sigmoid(x))
            return x
        else:
            return x * torch.sigmoid(x)

class CoordAtt(nn.Module):
    def __init__(self, inp, oup, reduction=32, input_dim=3):
        super(CoordAtt, self).__init__()
        self.input_dim = input_dim
        self.pool_h = nn.AdaptiveMaxPool2d((None, 1))
        # AdaptiveMaxPool2d
        self.pool_w = nn.AdaptiveAvgPool2d((1, None))
        # AdaptiveMaxPool2d
        # 可以交替着

        mip = max(8, inp // reduction)

        self.conv1 = nn.Conv2d(inp, mip, kernel_size=1, stride=1, padding=0)
        self.bn1 = nn.BatchNorm2d(mip)
        # self.act = h_swish()
        # self.act = swish()
        self.act = ReLU()
        # swish() ReLU 尝试更换激活函数

        self.conv_h = nn.Conv2d(mip, oup, kernel_size=1, stride=1, padding=0)
        self.conv_w = nn.Conv2d(mip, oup, kernel_size=1, stride=1, padding=0)

        if self.input_dim == 3:
            # Define the dimension transformation layers
            self.dim_trans1 = nn.Linear(inp, oup)
            self.dim_trans2 = nn.Linear(oup, inp)

    def forward(self, x):
        identity = x

        n, c, h, w = x.size()
        x_h = self.pool_h(x)
        x_w = self.pool_w(x).permute(0, 1, 3, 2)

        y = torch.cat([x_h, x_w], dim=2)
        y = self.conv1(y)
        y = self.bn1(y)
        y = self.act(y)

        x_h, x_w = torch.split(y, [h, w], dim=2)
        x_w = x_w.permute(0, 1, 3, 2)

        a_h = self.conv_h(x_h).sigmoid()
        a_w = self.conv_w(x_w).sigmoid()

        out = identity * a_w * a_h

        if self.input_dim == 3:
            # Transform the input dimension from 3 to 4
            out = out.flatten(2)
            out = out.transpose(1, 2)
            out = self.dim_trans1(out)
            # Transform the output dimension from 4 to 3
            out = self.dim_trans2(out)
            out = out.transpose(1, 2)
            out = out.view(n, c, h, w)

        return out

